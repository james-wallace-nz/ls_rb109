=begin
Exercise 10:
Can hash values be arrays? Can you have an array of hashes? 
Give examples.
=end

# Yes hash values can be arrays:
cars = {subaru: ["Outback", "Forrester", "Impreza"],
        ford: ["F-150", "Focus", "Bronco"],
        toyota: ["Camry", "Prius", "Corolla"]}

# You can also have an array of hashes:
people = [{name: "Ginni", age: 32}, {name: "Jonathan", age: 33}, 
          {name: "Dylan", age: 35}]

